namespace Touchee.Media.Music {

    /// <summary>
    /// Describes the encoding used by some audio stream
    /// </summary>
    public enum StreamEncoding {
        /// <summary>
        /// High-Efficiency Advanced Audio Coding
        /// </summary>
        HEAAC,
        /// <summary>
        /// MPEG-1 or MPEG-2 Audio Layer III
        /// </summary>
        MP3,
        /// <summary>
        /// Windows Media Audio
        /// </summary>
        WindowsMediaAudio
    }

}
